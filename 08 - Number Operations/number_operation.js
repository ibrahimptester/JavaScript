/* 
basic number operations: Addition, Subtraction, Multiplication, Division
*/

// Addition
let product1 = 20, product2 = 10;
console.log("Total price is:", product1 + product2);

// Subtraction
console.log("The difference between products is:", product1 - product2)

// Multiplication
let width = "100px", height = "300px";
let area = parseInt(width) * parseInt(height)
console.log(`The total area is ${area}px`)

// Division
let totalBill = 400;
let number_of_persons = 4;
console.log("Every persons share is:", totalBill / number_of_persons)
