/* 
The comparing operators are:
1. Equal to (==)
2. Not Equal to (!=)
3. Grater than (>)
4. Less than (<)
5. Greater than or Equal to (>=)
6. Less than or Equal to (<=)
*/

// Equal to Partial (==), Equal to Strict (===)
let a = 10; b = '10'
console.log("Partial comparison:", a==b)
console.log("Strict comparison:", a === b);

// Greater than 
let g = 110; h = 10;
console.log("110 is greater than 10:", g>h);

// less than or equal to 
console.log("110 is lesser or equal to 10:", g <= h);



