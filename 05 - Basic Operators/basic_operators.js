/* 
Basic Operator list:
Arithmetic Operators : 
1. Addition (+)
2. Subtraction (-)
3. Multiplication (*)
4. Division (/)
5. Modulus (%)
6. Exponentiation (**)
*/

// Addition
let a = 10; b = 5;
console.log("Addition of a + b is:", a + b);

// Subtraction
console.log("Subtraction of a - b is:", a - b);

// Multiplication
console.log("Multiplication of a * b is:", a * b);

// Division
console.log("Division of a / b is:", a / b);

// Modulus 
console.log("Modulus of a % b is:", a % b);

// exponentiation 10^3
let x = 10; y = 3;
console.log('Exponentiation of a ** b is:', x ** y);

